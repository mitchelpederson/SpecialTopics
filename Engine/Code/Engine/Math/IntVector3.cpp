#include "Engine/Math/IntVector3.hpp"


//----------------------------------------------------------------------------------------------------------------
IntVector3::IntVector3() : x(0), y(0), z(0) {}


//----------------------------------------------------------------------------------------------------------------
IntVector3::IntVector3( int x, int y, int z ) 
	: x(x)
	, y(y)
	, z(z)
{}


//----------------------------------------------------------------------------------------------------------------
IntVector3 operator+( const IntVector3& a, const IntVector3& b ) {
	return IntVector3( a.x + b.x, a.y + b.y, a.z + b.z );
}


//----------------------------------------------------------------------------------------------------------------
IntVector3 operator-( const IntVector3& a, const IntVector3& b ) {
	return IntVector3( a.x - b.x, a.y - b.y, a.z - b.z );
}